package com.ciclos;

class Ciclos1_JARG {

	public static void main(String[] args) {
		// Programa un algoritmo que realice la tabla de multiplicar del 12
		
		int x = 12;
		int res;
		System.out.println("Tabla del 12");
		for (int i = 1; i <= 10; i++) {
			res = x * i;
			System.out.println(res);
		}

	}

}
